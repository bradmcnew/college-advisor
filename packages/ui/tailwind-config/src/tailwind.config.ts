/**
 * Create a Tailwind config by merging the shared config with package-specific settings
 */
export function createTailwindConfig(packageConfig: Partial<Config> = {}): Config {
  return {
    content: [],
    ...sharedTailwindConfig,
    ...packageConfig,
    theme: {
      ...sharedTailwindConfig.theme,
      ...packageConfig.theme,
      extend: {
        ...sharedTailwindConfig.theme?.extend,
        ...packageConfig.theme?.extend,
      },
    },
    plugins: [...(sharedTailwindConfig.plugins ?? []), ...(packageConfig.plugins ?? [])],
  } as Config
}

